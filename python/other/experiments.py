### lambdda function
double = lambda x: x*2
print(double(5))

## this = labmda function
def double(x):
    return x*2
print(double(5))

### the shorten variant of if and else
number = 4
print("Even" if number % 2 == 0 else "Odd")
## this is equivalent variant
number = 4
if number % 2 == 0:
    print("Even")
else:
    print("Odd")

